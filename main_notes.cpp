#include <iostream>

using namespace std;

int
main ()				// cout is used to perform output on the standard output device which is usually the display screen. cout is used in combination with the insertion operator <<. Note that you can add multiple insertion operators after cout.
{
  cout << "Hello World";
  cout << " This " << "is " << "awesome!";	// In C++, the semicolon is used to terminate a statement. Each statement must end with a semicolon. It indicates the end of one logical expression.

  return 0;
}
